public class Dowhileloop {
    // i want to execute this block once even if condition is false.
    // do while loop will execute the condition once even if the condition is false.

    public static void main(String[] args) {
        int i =5;
        do
        {
            System.out.println("Hi " + i);
            i++;
        }while (i<=4);
    }
    
}
